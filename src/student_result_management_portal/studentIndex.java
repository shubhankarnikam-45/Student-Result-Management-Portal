    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        
        //roll number.
        String rollNumber = rollNoTextField.getText();
        
         try
        {
            //created the the Driver class of database.
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbSrms","root","Database@12345");

            //this line to use the 'command' in our project.
            Statement st = conn.createStatement();
            
            //we add result of those student whose roll number is exits.
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo = '"+rollNumber+"' and result.rollNo = '"+rollNumber+"'");
            
            if(rs.next())
            {
                courseNameTextField.setText(rs.getString(2));
                branchNameTextField.setText(rs.getString(3));
                studentNameTextField.setText(rs.getString(4));
                rollNoTextField.setText(rs.getString(1));
                fatherNameTextField.setText(rs.getString(6));
                genderTextField.setText(rs.getString(5));
                
                physicsMarksTextField.setText(rs.getString(8));
                mathsMarksTextField.setText(rs.getString(9));
                computerTextField.setText(rs.getString(10));
                dbmsTextField.setText(rs.getString(11));
                osTextField.setText(rs.getString(12));
                
                int physicsMarks = Integer.parseInt(physicsMarksTextField.getText());
                int mathsMarks =  Integer.parseInt(mathsMarksTextField.getText());
                int computerMarks = Integer.parseInt(computerTextField.getText());
                int dbmsMarks = Integer.parseInt(dbmsTextField.getText());
                int osMarks = Integer.parseInt(osTextField.getText());
                
                int totalMarks =physicsMarks + mathsMarks+computerMarks +dbmsMarks + osMarks;
                
                String strTotalMarks = Integer.toString(totalMarks);
                
                totalMarksTextField.setText(strTotalMarks);
                
                
                      
            }
           
        }
         catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

