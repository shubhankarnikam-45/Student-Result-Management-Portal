    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        //extracting the roll number
        String rollNo = jTextField1.getText();
        
        
           //try catch to handle the exception.
        try
        {
            //created the the Driver class of database.
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbSrms","root","Database@12345");

            //this line to use the 'command' in our project.
            Statement st = conn.createStatement();
            
            //we add result of those student whose roll number is exits.
            ResultSet rs = st.executeQuery("select * from result where result.rollNo = '"+rollNo+"'");
            
            if(rs.next())
            {
//                JOptionPane.showMessageDialog(null,"Message Saved Sucessfully");    
                setVisible(false);
                studentIndex studentIndexPage = new studentIndex(rollNo);
                studentIndexPage.setVisible(true);
                studentIndexPage.setLocationRelativeTo(null);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"This is not registered Roll Number");
            }
        }
         catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_searchButtonActionPerformed

