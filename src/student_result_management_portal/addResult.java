    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String rollNo = rollNoTextField.getText();
        
        String physics = physicsMark.getText();

        String maths = mathsMark.getText();

        String cn = computerNetworkMark.getText();
        
        String dbms = dbmsMark.getText();

        String os = osMark.getText();

        

        //try catch to handle the exception.
        try
        {
            //created the the Driver class of database.
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbSrms","root","Database@12345");

            //this line to use the 'command' in our project.
            Statement st = conn.createStatement();
            
            //we add result of those student whose roll number is exits.
            ResultSet rs = st.executeQuery("select * from student where rollNo='"+rollNo+"'");
            
            if(rs.next())
            {
                st.executeUpdate("insert into result(rollNo, physics, mathematics, computer, dbms, os) values ('"+rollNo+"','"+physics+"','"+maths+"','"+cn+"','"+dbms+"','"+os+"')");
                 //sucessful message.
                JOptionPane.showMessageDialog(null,"Message Saved Sucessfully");    
                setVisible(false);
                new adminHome().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"This is not registered Roll Number");
            }
                    
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

