    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String course = (String)courseComboBox.getSelectedItem();
        
        String branchName = (String)branchNameComboBox.getSelectedItem();
        
        String rollNo = rollNumberButton.getText();
        
        String name = nameButton.getText();
        
        String fatherName = fatherNameButton.getText();
        
        String gender = (String)genderComboBox.getSelectedItem();
        
        if(course.equals("") || branchName.equals("") || rollNo.equals("") || name.equals("") ||  fatherName.equals("") || gender.equals(""))
        {
            JOptionPane.showMessageDialog(null,"some fields data is missing");
            return;
        }
        
        //try catch to handle the exception.
        try
        {
            //created the the Driver class of database.
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbSrms","root","Database@12345");
            
            
            //this line to use the 'command' in our project.
            Statement st = conn.createStatement();
            st.executeUpdate("insert into student(rollNo, course, branchName, studName, gender, fatherName) values ('"+rollNo+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+fatherName+"')");
            
            //sucessful message.
            JOptionPane.showMessageDialog(null,"Message Saved Sucessfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
             JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

